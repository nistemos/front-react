"use client";
import { ToastContainer, toast } from "react-toastify";

const Toast = () => {
  return <ToastContainer />;
};

export const showToastInfo = (message) => {
  toast.info(message, {
    position: "bottom-right",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",
  });
};

